%%
% Set the parameters of the three link biped. The parameters include the
% masses m1, m2, m3, and the link lenghts l1, l2, l3 and the gravity
% acceleration value g
function [m1, m2, m3, l1, l2, l3, g] = set_parameters()

    m1 = 7;
    m2 = 7;
    m3 = 17;
    
    l1 = 0.5;
    l2 = 0.5;
    l3 = 0.35;
    
    g = 9.81;

end